package jan_pac1;

public class forloop {

	public static void main(String[] args) {
		
		
		for(int i=0;i<5;i++)
		{
		System.out.println("increment value :"+i);
		
		}
		System.out.println("****************************");
		for( int j=5;j>1;j--)
		{
		
		System.out.println("Decrement value :"+j);
		}

	}

}
